package chapter2;

public class Conversion {

	public static void main(String[] args) {
		int iNum = 10;
		byte bNum = (byte) iNum;
		System.out.println(bNum);
		
		int iNum2 = 1000;
		byte bNum2 = (byte)iNum2;
		System.out.println(bNum2);
		
		double dNum = 3.14;
		int iNum3 = (int)dNum;
		System.out.println(iNum3);
	}

}
